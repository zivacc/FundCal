/**
 * 根据 data/allfund/allfund.json 构建「联接基金」(feeder fund) 与「母基金」(master fund) 索引
 * 便于快速查找：某基金对应的母基金（场内 ETF/LOF）及同一母基金下的所有联接份额。
 *
 * 命名：联接基金 = feeder fund，对应母基金 = master fund
 *
 * 规则：
 * - 视为联接基金 (feeder)：基金名称含「联接」，或 crawled 的 fundType 中含「联接」（如「指数型-ETF联接」）；另：类型含「联接」且名称以 A/B/C/D 份额结尾（如「沪深300ETFA」）也视为联接份额；须能解析出 masterKey 才入组
 * - masterKey：名称中含「联接」时，取「联接」前的部分（如「华夏沪深300ETF」）；若名称不含「联接」但 fundType 含「联接」且名称以 A/B/C/D（可选「类」「份额」）结尾，则去掉该份额后缀作为 key（如「沪深300ETFA」→「沪深300ETF」）；无法解析则跳过
 * - 同一 masterKey 下的所有联接代码归为一组；若全市场中存在名称等于 masterKey 的基金，视为母基金 (master)
 * - 若存在 data/allfund/feeder-master-overrides.json，则按其中 masterKey -> 母基金代码 覆盖（用于场内名与联接名不一致的情况，如中银上海金ETF联接 -> 518660 工银瑞信黄金ETF）
 *
 * 输出：data/allfund/feeder-index.json
 * 使用：node scripts/build-feeder-index.js
 */

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ALLFUND_DIR = path.join(__dirname, '..', 'data', 'allfund');
const ALLFUND_PATH = path.join(ALLFUND_DIR, 'allfund.json');
const FEEDER_INDEX_PATH = path.join(ALLFUND_DIR, 'feeder-index.json');
const FEEDER_MASTER_OVERRIDES_PATH = path.join(ALLFUND_DIR, 'feeder-master-overrides.json');

/** 名称是否以 A/B/C/D 基金份额结尾（含可选「类」「份额」） */
function hasAbcdShareSuffix(name) {
  const n = (name || '').trim();
  if (!n) return false;
  return /[ABCD](?:类|份额)?$/i.test(n);
}

/**
 * 从基金名称 + 类型提取母基金 key
 * - 默认：「联接」之前的部分
 * - 补充：类型含「联接」但简称未写「联接」、仅以 A/B/C/D 结尾时，去掉份额后缀
 */
function getMasterKey(name, fundType = '') {
  if (!name || typeof name !== 'string') return '';
  const n = name.trim();
  const t = (fundType || '').trim();
  const idx = n.indexOf('联接');
  if (idx !== -1) return n.slice(0, idx).trim();
  if (t.includes('联接') && hasAbcdShareSuffix(n)) {
    return n.replace(/[ABCD](?:类|份额)?$/i, '').trim();
  }
  return '';
}

/** 是否联接基金：名称或基金类型含「联接」（类型含「联接」时，即使简称仅以 A/B/C/D 结尾、未写「联接」二字，亦为 true；母基金 key 见 getMasterKey） */
function isFeederFund(name, fundType) {
  const n = (name || '').trim();
  const t = (fundType || '').trim();
  if (n.includes('联接')) return true;
  if (t.includes('联接')) return true;
  return false;
}

function main() {
  if (!fs.existsSync(ALLFUND_PATH)) {
    console.warn('allfund.json 不存在，将生成空联接索引');
    const empty = {
      description: 'Feeder fund (联接基金) and master fund (母基金) index. Generated by build-feeder-index.js',
      feederByMasterKey: {},
      codeToFeeder: {}
    };
    fs.mkdirSync(ALLFUND_DIR, { recursive: true });
    fs.writeFileSync(FEEDER_INDEX_PATH, JSON.stringify(empty, null, 2), 'utf8');
    return;
  }

  const all = JSON.parse(fs.readFileSync(ALLFUND_PATH, 'utf8'));
  const funds = all.funds || {};
  const codes = Array.isArray(all.codes) ? all.codes : Object.keys(funds);

  /** masterKey -> { masterCode, masterName, feederCodes[] } */
  const feederByMasterKey = {};
  /** code -> { masterKey, isFeeder, masterCode?, masterName?, feederCodes? } 便于按代码快速查 */
  const codeToFeeder = {};

  // 1) 收集所有联接基金，按 masterKey 分组
  for (const code of codes) {
    const f = funds[code];
    if (!f) continue;
    const name = (f.name || '').trim() || '';
    const fundType = (f.fundType || '').trim();
    if (!isFeederFund(name, fundType)) continue;
    const masterKey = getMasterKey(name, fundType);
    if (!masterKey) continue;
    if (!feederByMasterKey[masterKey]) {
      feederByMasterKey[masterKey] = { masterCode: null, masterName: null, feederCodes: [] };
    }
    feederByMasterKey[masterKey].feederCodes.push(String(code));
  }

  // 2) 为每个 masterKey 匹配母基金（名称完全等于 masterKey 的基金，且非联接）
  for (const code of codes) {
    const f = funds[code];
    if (!f) continue;
    const name = (f.name || '').trim() || '';
    const fundType = (f.fundType || '').trim();
    if (isFeederFund(name, fundType)) continue;
    if (!feederByMasterKey[name]) continue;
    const entry = feederByMasterKey[name];
    if (entry.masterCode == null) {
      entry.masterCode = String(code);
      entry.masterName = name;
    }
  }

  // 2b) 应用母基金覆盖：联接名与场内名不一致时，用 feeder-master-overrides.json 指定 masterKey -> 母基金代码
  if (fs.existsSync(FEEDER_MASTER_OVERRIDES_PATH)) {
    try {
      const overridesData = JSON.parse(fs.readFileSync(FEEDER_MASTER_OVERRIDES_PATH, 'utf8'));
      const overrides = overridesData.overrides || {};
      for (const [masterKey, masterCode] of Object.entries(overrides)) {
        const code = String(masterCode).trim();
        if (!code || code.length !== 6) continue;
        const entry = feederByMasterKey[masterKey];
        if (!entry) continue;
        entry.masterCode = code;
        entry.masterName = (funds[code] && funds[code].name) ? funds[code].name.trim() : code;
      }
      console.log(`已应用母基金覆盖 ${Object.keys(overrides).length} 条（来自 feeder-master-overrides.json）`);
    } catch (e) {
      console.warn('读取或应用 feeder-master-overrides.json 失败，已忽略：', e.message || e);
    }
  }

  // 3) 去重并排序 feederCodes
  for (const key of Object.keys(feederByMasterKey)) {
    feederByMasterKey[key].feederCodes = [...new Set(feederByMasterKey[key].feederCodes)].sort();
  }

  // 4) 构建按代码的快速查找：联接基金
  for (const [masterKey, entry] of Object.entries(feederByMasterKey)) {
    for (const code of entry.feederCodes) {
      codeToFeeder[code] = {
        masterKey,
        isFeeder: true,
        masterCode: entry.masterCode,
        masterName: entry.masterName,
        feederCodes: entry.feederCodes
      };
    }
  }

  // 5) 母基金：若某基金是某 masterKey 的 master，则也写入 codeToFeeder，便于「从母基金查所有联接」
  for (const [masterKey, entry] of Object.entries(feederByMasterKey)) {
    if (entry.masterCode) {
      codeToFeeder[entry.masterCode] = {
        masterKey,
        isFeeder: false,
        masterCode: entry.masterCode,
        masterName: entry.masterName,
        feederCodes: entry.feederCodes
      };
    }
  }

  const payload = {
    description: 'Feeder (联接) and master (母基金) index. feederByMasterKey: lookup by master name; codeToFeeder: lookup by fund code.',
    updatedAt: new Date().toISOString(),
    feederByMasterKey,
    codeToFeeder
  };

  fs.mkdirSync(ALLFUND_DIR, { recursive: true });
  fs.writeFileSync(FEEDER_INDEX_PATH, JSON.stringify(payload, null, 2), 'utf8');

  const feederCount = Object.keys(codeToFeeder).filter(c => codeToFeeder[c].isFeeder).length;
  const masterMatchedCount = Object.values(feederByMasterKey).filter(e => e.masterCode != null).length;
  console.log(`已生成 ${FEEDER_INDEX_PATH}：联接基金(feeder) ${feederCount} 只，母基金组 ${Object.keys(feederByMasterKey).length} 组，已匹配母基金(master) ${masterMatchedCount} 组。`);
}

main();
